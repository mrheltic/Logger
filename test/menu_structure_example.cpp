void setup()
{

  // INITIALIZING SERIAL MONITOR
  Serial.begin(115200);

  while (!Serial)
  {

    ; // wait for serial port to connect. Needed for native USB port only
  }

  Serial.println("Initializing...");

  // INITIALIZING BUTTONS
  pinMode(BUTTON_1, INPUT);
  pinMode(BUTTON_2, INPUT);
  pinMode(BUTTON_3, INPUT);
  Serial.println("Buttons initialized");

  pinMode(BUZZER, OUTPUT);

  // SSD1306_SWITCHCAPVCC = generate display voltage from 3.3V internally
  if (!display.begin(SSD1306_SWITCHCAPVCC, 0x3C))
  {

    Serial.println(F("SSD1306 allocation failed"));

    for (;;)
      ; // Don't proceed, loop forever
  }

  // INITIALIZING SCREEN
  Serial.println("Display initialized");
  display.clearDisplay();

  // DISPLAYING TEXT ON SCREEN
  // display.clearDisplay();
  display.setTextSize(1);
  display.setTextColor(WHITE);
  display.setCursor(0, 0);

  display.println("Display initialized");
  Serial.println("Printing on screen");

  display.display();

  Serial.println("ADC initialized");
  display.println("ADC initialized");
  delay(400);
  display.display();
  display.clearDisplay();
  updateMenu();
}

void loop()
{
  if (stataMenu)
  {
    if (digitalRead(BUTTON_1))
    {
      delay(100);
      menu++;
      updateMenu();
    }

    if (digitalRead(BUTTON_3))
    {
      delay(100);
      menu--;
      updateMenu();
    }

    if (digitalRead(BUTTON_2))
    {
      stataMenu = 0;
      delay(250);
      executeAction();
    }
  }
  Serial.println("Sono nel loop generale");

  Serial.println(digitalRead(BUTTON_2));
  Serial.print("state menu: ");
  Serial.println(stataMenu);
  Serial.print("menu ");
  Serial.println(menu);
  Serial.println();
  //delay(50);
}